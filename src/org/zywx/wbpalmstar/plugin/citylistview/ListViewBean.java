package org.zywx.wbpalmstar.plugin.citylistview;

public class ListViewBean {

	private String bgColor;
	private String separatorLineColor;
	private String sectionHeaderTitleColor;
	private String sectionHeaderBgColor;
	private String itemTextColor;
	public String getBgColor() {
		return bgColor;
	}
	public void setBgColor(String bgColor) {
		this.bgColor = bgColor;
	}
	public String getSeparatorLineColor() {
		return separatorLineColor;
	}
	public void setSeparatorLineColor(String separatorLineColor) {
		this.separatorLineColor = separatorLineColor;
	}
	public String getSectionHeaderTitleColor() {
		return sectionHeaderTitleColor;
	}
	public void setSectionHeaderTitleColor(String sectionHeaderTitleColor) {
		this.sectionHeaderTitleColor = sectionHeaderTitleColor;
	}
	public String getSectionHeaderBgColor() {
		return sectionHeaderBgColor;
	}
	public void setSectionHeaderBgColor(String sectionHeaderBgColor) {
		this.sectionHeaderBgColor = sectionHeaderBgColor;
	}
	public String getItemTextColor() {
		return itemTextColor;
	}
	public void setItemTextColor(String itemTextColor) {
		this.itemTextColor = itemTextColor;
	}
	
	
}
